#include<stdio.h>
int main(){
    int arr[5];
    // get a array value from the user
    for(int a=0;a<5;a++){
        printf("Enter the Number:");
        scanf("%d",&arr[a]);
    }
    printf("Array Values:\n");
    // Print the array
    for(int i=0;i<5;i++){
        printf("%d\n",arr[i]);
    }
    return 0;
}

